public class Switchcase {
    public static void main(String [] args) {
        int day = 5;
       switch (day){
           case 1:
               System.out.println("its monday, 1st");
               break;
               // if
           case 2:
               System.out.println("its tue, 2nd");
               break;
               // else if
           case 3:
               System.out.println("its wed, 3rd");
               break;
           case 4:
               System.out.println("its thu, 4th");
               break;
           case 5:
               System.out.println("its fri, 5th");
               break;
           case 6:
               System.out.println("its sat, 6th");
               break;
           case 7:
               System.out.println("its sun, 7th");
               break;
           default:
               System.out.println("default");
               break;
               // else

       }

    }
}
